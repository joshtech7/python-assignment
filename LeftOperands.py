left_operand = 27.5
right_operand = 2

print(left_operand + right_operand)  # Output: 29.5
print(left_operand - right_operand)  # Output: 25.5
print(left_operand * right_operand)  # Output: 55.0
print(left_operand / right_operand)  # Output: 13.75
print(left_operand // right_operand)  # Output: 13.0
print(left_operand ** right_operand)  # Output: 756.25